package com.java;

public class SwitchStatement {

	public static void main(String[] args){
		
	    //int a = Integer.parseInt(args[1]);
		int a=1;
	    
	    switch (a) {
	    case 1:
	        System.out.println("FRI"); 
	        break;
	    case 2:
	        System.out.println("MON"); 
	        break;
	    case 3:
	        System.out.println("TUE"); 
	        break;
	    default:
	        System.out.println("Invalid No"); 
	        break;
	    }
	  }

}
