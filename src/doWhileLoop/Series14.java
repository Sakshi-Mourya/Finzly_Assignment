//// Generate the Series... 1 2 3 4 5 4 3 2 1.
package doWhileLoop;

public class Series14 {

	public static void main(String[] args) {
		int i=1;
		 do{
			System.out.print(i+" ");
			i++;
		}while(i<=5);
		 
		int j=4;
		 do{
			System.out.print(j+" ");
			j--;
		}while(j>=1);
	}

}
